package Data1;

public class Keys {
	private String key="";
	private int dip=0;
	Keys(String k,int d){
		this.key=k;
		this.dip=d;
	}
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	public int getDip() {
		return dip;
	}
	public void setDip(int dip) {
		this.dip = dip;
	}
	
}
